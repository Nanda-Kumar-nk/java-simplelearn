package checkstring;

/**
 * @author Nanda Kumar
 * @since 27/06/2022
 */
public class SomeTest {

    private String value;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "SomeTest{" +
                "value='" + value + '\'' +
                '}';
    }
}
